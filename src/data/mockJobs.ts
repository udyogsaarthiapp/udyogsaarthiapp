// Additional utility functions for job data
export const getJobsByType = (type: Job['type']): Job[] => {
  return mockJobs.filter(job => job.type === type);
};

export const getFeaturedJobs = (): Job[] => {
  return mockJobs.filter(job => job.featured);
};

export const getJobsByLocation = (location: string): Job[] => {
  return mockJobs.filter(job => job.location === location);
};

export const getJobsBySkill = (skill: string): Job[] => {
  return mockJobs.filter(job => 
    job.skills.some(jobSkill => 
      jobSkill.toLowerCase().includes(skill.toLowerCase())
    )
  );
};

export const getUniqueLocations = (): string[] => {
  return Array.from(new Set(mockJobs.map(job => job.location))).sort();
};

export const getUniqueSkills = (): string[] => {
  const allSkills = mockJobs.flatMap(job => job.skills);
  return Array.from(new Set(allSkills)).sort();
};

export const getUniqueCategories = (): string[] => {
  const categories = mockJobs.map(job => job.category).filter((category): category is string => Boolean(category));
  return Array.from(new Set(categories)).sort();
};